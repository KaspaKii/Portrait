    #[test]
    fn expr_is_onchain_only_detects_builtins() {
        let sig = portrait_syntax::parse_expr("checkSig(auth, owner)").unwrap();
        assert!(expr_is_onchain_only(&sig));
        // Nested builtin inside a boolean expression is still on-chain-only.
        let nested = portrait_syntax::parse_expr("checkSig(auth, owner) && seq >= 0").unwrap();
        assert!(expr_is_onchain_only(&nested));
        // A pure arithmetic comparison is assertable (NOT on-chain-only).
        let cmp = portrait_syntax::parse_expr("batch_count >= 1").unwrap();
        assert!(!expr_is_onchain_only(&cmp));
    }
